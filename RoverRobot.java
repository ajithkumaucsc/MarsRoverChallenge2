package com.MarsRover.domain;

public class RoverRobot {

   
    private int roverX;
    private int roverY;
    private char direction;
    private String roverCommand;
    
	public int getRoverX() {
		return roverX;
	}
	public void setRoverX(int roverX) {
		this.roverX = roverX;
	}
	public int getRoverY() {
		return roverY;
	}
	public void setRoverY(int roverY) {
		this.roverY = roverY;
	}
	public char getDirection() {
		return direction;
	}
	public void setDirection(char direction) {
		this.direction = direction;
	}
	public String getRoverCommand() {
		return roverCommand;
	}
	public void setRoverCommand(String roverCommand) {
		this.roverCommand = roverCommand;
	}
	
	
}
